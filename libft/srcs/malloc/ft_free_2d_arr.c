#include "libft.h"

void	ft_free_2d_arr(void ***ptr)
{
	int	i;

	i = 0;
	while ((*ptr)[i])
	{
		ft_free(&(*ptr)[i]);
		i++;
	}
	free(*ptr);
	*ptr = NULL;
}
